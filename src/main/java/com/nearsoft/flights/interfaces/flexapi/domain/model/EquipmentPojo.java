package com.nearsoft.flights.interfaces.flexapi.domain.model;

public class EquipmentPojo {
	private String iata;
	private String name;
	private String turboProp;
	private String jet;
	private String widebody;
	private String regional;
	
	public EquipmentPojo() {}

	public String getIata() {
		return iata;
	}

	public String getName() {
		return name;
	}

	public String getTurboProp() {
		return turboProp;
	}

	public String getJet() {
		return jet;
	}

	public String getWidebody() {
		return widebody;
	}

	public String getRegional() {
		return regional;
	}
}
